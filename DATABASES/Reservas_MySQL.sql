            SELECT id FROM pistas WHERE id_polideportivo IN  (
                SELECT id FROM polideportivos WHERE ciudad = 'Huesca'
            )
        )
    )
);

-- ACTUALIZACION DEL PRECIO DE LAS PISTAS
-- Cuestan 25€ las pistas de futbol que han sido reservadas alguna vez
UPDATE pistas SET precio = 25 WHERE tipo = 'futbol';
-- Cuestan 20€ las pistas de tenis o baloncesto que han sido reservadas alguna vez
UPDATE pistas SET precio = 20 WHERE tipo IN ('tenis', 'baloncesto');
-- Cuestan 15€ las pistas de ping-pong que han sido reservadas alguna vez
UPDATE pistas SET precio = 15 WHERE tipo = 'ping-pong';
-- Cuestan 10€ menos las pistas que no han sido reservadas ninguna vez
UPDATE pistas SET precio = precio - 10 WHERE id NOT IN (SELECT id_pista FROM reservas);
-- Se hace un 20% de descuento a las pistas que tienen reservas anteriores a 2014
UPDATE pistas SET precio = precio * 0.8 WHERE id IN (SELECT id_pista FROM reservas WHERE fecha_reserva < '2014-01-01');
-- Se hace un 30% de descuento a las pistas reservadas mas de una vez
UPDATE pistas SET precio = precio * 0.7 WHERE id IN (SELECT id_pista FROM reservas GROUP BY id_pista HAVING COUNT(*) > 1);
