--
-- Database: reservas
--
DROP DATABASE IF EXISTS reservas;
CREATE DATABASE IF NOT EXISTS reservas;
USE reservas;

-- --------------------------------------------------------

--
--  Creación de la tabla pistas
--

CREATE TABLE IF NOT EXISTS pistas (
  id INT UNSIGNED AUTO_INCREMENT,
  tipo ENUM('tenis','futbol','baloncesto','ping-pong') NOT NULL,
  precio FLOAT DEFAULT 0,
  id_polideportivo INT UNSIGNED NOT NULL,
  PRIMARY KEY (id)
);

-- --------------------------------------------------------

--
-- Creación de la tabla pistas_abiertas
--

CREATE TABLE IF NOT EXISTS pistas_abiertas (
  id_pista INT UNSIGNED NOT NULL,
  operativa TINYINT(1) DEFAULT 1,
  fecha_ultima_reserva DATETIME DEFAULT NULL,
  proxima_revision DATETIME NOT NULL,
  PRIMARY KEY (id_pista)
);

-- --------------------------------------------------------

--
-- Creación de la tabla pistas_cerradas
--

CREATE TABLE IF NOT EXISTS pistas_cerradas (
  id_pista INT UNSIGNED NOT NULL,
  fecha_clausura DATE NOT NULL,
  motivo VARCHAR(500) NOT NULL,
  PRIMARY KEY (id_pista)
);

-- --------------------------------------------------------

--
-- Creación de la tabla polideportivos
--

CREATE TABLE IF NOT EXISTS polideportivos (
  id INT UNSIGNED AUTO_INCREMENT,
  nombre VARCHAR(75) NOT NULL,
  direccion VARCHAR(100) NOT NULL,
  ciudad VARCHAR(100) NOT NULL,
  extension FLOAT DEFAULT 0,
  PRIMARY KEY (id)
);

-- --------------------------------------------------------

--
-- Creación de la tabla reservas
--

CREATE TABLE IF NOT EXISTS reservas (
  id INT UNSIGNED AUTO_INCREMENT,
  fecha_reserva TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  fecha_uso DATETIME NOT NULL,
  precio FLOAT DEFAULT 0,
  id_pista INT UNSIGNED DEFAULT NULL,
  PRIMARY KEY (id)
);

-- --------------------------------------------------------

--
-- Creación de la tabla usuario_reserva
--

CREATE TABLE IF NOT EXISTS usuario_reserva (
  dni_usuario CHAR(9) NOT NULL,
  id_reserva INT UNSIGNED NOT NULL,
  asiste TINYINT(1) NOT NULL DEFAULT 0,
  PRIMARY KEY (dni_usuario,id_reserva)
);

-- --------------------------------------------------------

--
-- Creación de la tabla usuario_usuario
--

CREATE TABLE IF NOT EXISTS usuario_usuario (
  dni_usuario CHAR(9) NOT NULL,
  dni_amigo CHAR(9) NOT NULL,
  PRIMARY KEY (dni_usuario,dni_amigo)
);

-- --------------------------------------------------------

--
-- Creación de la tabla usuarios
--

CREATE TABLE IF NOT EXISTS usuarios (
  dni CHAR(9) NOT NULL,
  nombre VARCHAR(50) NOT NULL,
  apellidos VARCHAR(50) NOT NULL,
  email VARCHAR(50) UNIQUE NOT NULL,
  ciudad VARCHAR(255) NOT NULL,
  fecha_nacimiento DATE NOT NULL,
  descuento FLOAT DEFAULT 0,
  fecha_alta TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (dni)
);

-- --------------------------------------------------------

--
-- Añadir las claves foráneas
--

ALTER TABLE pistas
  ADD CONSTRAINT pistas_ibfk_1 FOREIGN KEY (id_polideportivo) REFERENCES polideportivos (id) ON DELETE CASCADE ON UPDATE NO ACTION;

ALTER TABLE pistas_abiertas
  ADD CONSTRAINT pistas_abiertas_ibfk_1 FOREIGN KEY (id_pista) REFERENCES pistas (id) ON DELETE CASCADE ON UPDATE NO ACTION;

ALTER TABLE pistas_cerradas
  ADD CONSTRAINT pistas_cerradas_ibfk_1 FOREIGN KEY (id_pista) REFERENCES pistas (id) ON DELETE CASCADE ON UPDATE NO ACTION;

ALTER TABLE reservas
  ADD CONSTRAINT reservas_ibfk_1 FOREIGN KEY (id_pista) REFERENCES pistas (id) ON DELETE NO ACTION ON UPDATE NO ACTION;

ALTER TABLE usuario_reserva
  ADD CONSTRAINT usuario_reserva_ibfk_1 FOREIGN KEY (dni_usuario) REFERENCES usuarios (dni) ON DELETE NO ACTION ON UPDATE NO ACTION,
  ADD CONSTRAINT usuario_reserva_ibfk_2 FOREIGN KEY (id_reserva) REFERENCES reservas (id) ON DELETE CASCADE ON UPDATE NO ACTION;

ALTER TABLE usuario_usuario
  ADD CONSTRAINT usuario_usuario_ibfk_1 FOREIGN KEY (dni_usuario) REFERENCES usuarios (dni) ON DELETE CASCADE ON UPDATE NO ACTION,
  ADD CONSTRAINT usuario_usuario_ibfk_2 FOREIGN KEY (dni_amigo) REFERENCES usuarios (dni) ON DELETE CASCADE ON UPDATE NO ACTION;

