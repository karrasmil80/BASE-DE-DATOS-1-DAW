-- ACTUALIZACION DE LA CIUDAD DE LOS USUARIOS
-- Establecer como Zaragoza la ciudad de los usuarios que hayan reservado alguna vez en esa ciudad
UPDATE usuarios SET ciudad = 'Zaragoza' WHERE dni IN (
    SELECT dni_usuario FROM usuario_reserva WHERE id_reserva IN (
        SELECT id FROM reservas WHERE id_pista IN (
            SELECT id FROM pistas WHERE id_polideportivo IN  (
                SELECT id FROM polideportivos WHERE ciudad = 'Zaragoza'
            )
        )
    )
);
-- Establecer como Teruel la ciudad de los usuarios que hayan reservado alguna vez en esa ciudad
